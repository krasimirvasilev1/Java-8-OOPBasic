package FirstExersiceDefiningClasses.CatLady;

public class TypeOfCat {
    Cymric cymric;
    Siamese siamese;
    StreetExtraOrdinary streetExtraOrdinary;

    public TypeOfCat() {
    }

    public Cymric getCymric() {
        return cymric;
    }

    public void setCymric(Cymric cymric) {
        this.cymric = cymric;
    }

    public Siamese getSiamese() {
        return siamese;
    }

    public void setSiamese(Siamese siamese) {
        this.siamese = siamese;
    }

    public StreetExtraOrdinary getStreetExtraOrdinary() {
        return streetExtraOrdinary;
    }

    public void setStreetExtraOrdinary(StreetExtraOrdinary streetExtraOrdinary) {
        this.streetExtraOrdinary = streetExtraOrdinary;
    }
}
